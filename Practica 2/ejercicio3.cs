/*3) ¿Qué diferencias existen entre las conversiones de tipo implícitas y explícitas?

Conversiones implícitas: son aquellas en las que no hace falta indicar entre paréntesis la conversion. 
Ejemplo:
double num = 10;

Conversiones explicitas: conocidas como "casting" / "casteo", son aquellas las cuales
es responsabilidad del programador especificar explicitamente la conversion de un tipo de dato a otro 
Ejemplo:
int num = (int) 10.0; Si no aclaro entre paréntesis el tipo de conversion me va a tirar error.

